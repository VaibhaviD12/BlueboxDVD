ALTER TABLE tbl_fnl_film_category ADD CONSTRAINT  PK_FM_ID PRIMARY KEY (film_id);
ALTER TABLE tbl_fnl_film_text ADD CONSTRAINT PK_FM_ID PRIMARY KEY (film_id);
ALTER TABLE tbl_fnl_inventory ADD CONSTRAINT PK_IN_ID PRIMARY KEY (inventory_id);
ALTER TABLE tbl_fnl_customer ADD CONSTRAINT PK_CUST_ID PRIMARY KEY (customer_id);
ALTER TABLE tbl_fnl_city ADD CONSTRAINT PK_IN_ID PRIMARY KEY (inventory_id);
