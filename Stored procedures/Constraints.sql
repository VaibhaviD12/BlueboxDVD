    (
    PROCESS_NAME,
    OPERATION_TYPE,
    OPERATED_BY,
	OPERATION_DATE)
    VALUES 
        (
        PROCESS_NAME,
        OPERATION_TYPE,
        CURRENT_USER(),
        CURRENT_TIMESTAMP());
        

END